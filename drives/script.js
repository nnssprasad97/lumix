/// Calculate total placements for each year
function calculateTotalPlacements() {
    var totals = {};
    for (var year in placementData) {
        var total = 0;
        for (var i = 0; i < placementData[year].length; i++) {
            total += placementData[year][i].total;
        }
        totals[year] = total;
    }
    return totals;
}

// Populate year dropdown
function populateYearDropdown() {
    var yearSelect = document.getElementById("year-select");
    if (!yearSelect) return;
    
    yearSelect.innerHTML = '<option value="">- - - - - Select Year - - - - -</option>';
    
    var years = Object.keys(placementData).sort().reverse();
    for (var i = 0; i < years.length; i++) {
        var option = document.createElement("option");
        option.value = years[i];
        option.textContent = years[i];
        yearSelect.appendChild(option);
    }
}

// --- NEW Function to display stats ---
function displayStats(year) {
    const statsContainer = document.getElementById("placement-stats-container");
    if (!statsContainer) return;

    let totalPlacements = 0;
    let companyCount = 0;
    let highestPlacement = { company: 'N/A', total: 0 };

    if (year && placementData[year]) {
        const yearData = placementData[year];
        companyCount = yearData.length;
        yearData.forEach(item => {
            totalPlacements += item.total;
            if (item.total > highestPlacement.total) {
                highestPlacement = item;
            }
        });
    } else { // Overall stats if no year is selected
        Object.values(placementData).forEach(yearData => {
            companyCount += yearData.length;
            yearData.forEach(item => {
                totalPlacements += item.total;
                if (item.total > highestPlacement.total) {
                    highestPlacement = item;
                }
            });
        });
    }

    statsContainer.innerHTML = `
        <div class="stats-card">
            <div class="stat-item">
                <div class="stat-icon placements"><i class="fas fa-user-check"></i></div>
                <div class="stat-info">
                    <h5>${totalPlacements.toLocaleString()}</h5>
                    <p>Total Placements</p>
                </div>
            </div>
        </div>
        <div class="stats-card">
            <div class="stat-item">
                <div class="stat-icon companies"><i class="fas fa-building"></i></div>
                <div class="stat-info">
                    <h5>${companyCount}</h5>
                    <p>Companies Participated</p>
                </div>
            </div>
        </div>
        <div class="stats-card">
            <div class="stat-item">
                <div class="stat-icon highest"><i class="fas fa-trophy"></i></div>
                <div class="stat-info">
                    <h5>${highestPlacement.total}</h5>
                    <p>by ${highestPlacement.company}</p>
                </div>
            </div>
        </div>
    `;
}


