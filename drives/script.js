function initializeJourneySection() {
    const features = [
        {
            step: 'Step 1',
            title: 'Get an Idea',
            content: 'Discover your passion and find the perfect project idea to kickstart your journey.',
            image: 'images/New idea-bro.png'

        },
        {
            step: 'Step 2',
            title: 'Learn and Grow',
            content: 'Acquire new skills and knowledge through our comprehensive learning resources.',
            image: 'images/Online learning-bro.png'
        },
        {
            step: 'Step 3',
            title: 'Get Placed',
            content: 'Land your dream job at a top company with your newly developed expertise.',
            image: 'images/Company-bro.png'
        }
    ];

    const autoPlayInterval = 3000; // 3 seconds
    let currentFeatureIndex = 0;
    let progress = 0;
    let intervalId;

    const featureListContainer = document.getElementById('feature-list');
    const imageContainer = document.getElementById('image-container');
    
    if (!featureListContainer || !imageContainer) return;

    function renderFeatures() {
        featureListContainer.innerHTML = '';
        features.forEach((feature, index) => {
            const isActive = index === currentFeatureIndex;
            const featureElement = document.createElement('div');
            featureElement.className = `feature-item flex items-center gap-6 md:gap-8 cursor-pointer ${isActive ? 'step-active' : 'step-inactive'}`;
            featureElement.dataset.index = index;
            featureElement.innerHTML = `
                <div class="flex-shrink-0">
                    <div class="w-10 h-10 md:w-12 md:h-12 rounded-full flex items-center justify-center border-2 transition-all duration-500 ${isActive ? 'bg-indigo-600 border-indigo-500 text-white scale-110' : 'bg-slate-700 border-slate-600 text-slate-400'}">
                        <span class="text-lg font-bold">${index < currentFeatureIndex ? '✓' : index + 1}</span>
                    </div>
                </div>
                <div class="flex-1">
                    <h3 class="text-xl md:text-2xl font-semibold text-slate-100">${feature.title}</h3>
                    <p class="text-base md:text-lg text-slate-400">${feature.content}</p>
                    ${isActive ? `<div class="mt-2 h-1 bg-slate-700 rounded-full overflow-hidden"><div class="progress-bar-fill h-1 bg-indigo-500 rounded-full" style="width: ${progress}%"></div></div>` : ''}
                </div>
            `;
            featureElement.addEventListener('click', () => {
                setCurrentFeature(index);
                resetInterval();
            });
            featureListContainer.appendChild(featureElement);
        });
    }

    function addNewImage(feature) {
        const newImage = document.createElement('img');
        newImage.src = feature.image;
        newImage.alt = feature.title;
        newImage.className = 'feature-image w-full h-full object-cover absolute inset-0 feature-image-enter';
        newImage.onerror = () => { newImage.src = 'https://placehold.co/1000x500/1e293b/94a3b8?text=Image+Not+Found'; };
        imageContainer.appendChild(newImage);
        setTimeout(() => {
            newImage.classList.remove('feature-image-enter');
        }, 20);
    }
    
    function renderImage() {
        const currentFeature = features[currentFeatureIndex];
        const existingImage = imageContainer.querySelector('.feature-image');
        if (existingImage) {
            existingImage.classList.add('feature-image-exit');
            existingImage.addEventListener('transitionend', () => {
                if (existingImage.parentElement) {
                    existingImage.remove();
                }
                addNewImage(currentFeature);
            }, { once: true });
        } else {
            addNewImage(currentFeature);
        }
    }

    function setCurrentFeature(index) {
        currentFeatureIndex = index;
        progress = 0;
        renderFeatures();
        renderImage();
    }
    
    function handleInterval() {
        progress += 100 / (autoPlayInterval / 100);
        if (progress >= 100) {
            progress = 100;
            const nextIndex = (currentFeatureIndex + 1) % features.length;
            setCurrentFeature(nextIndex);
        }
        renderFeatures();
    }

    function resetInterval() {
        clearInterval(intervalId);
        progress = 0;
        intervalId = setInterval(handleInterval, 100);
    }

    setCurrentFeature(0);
    resetInterval();
}


